// Practice question on increment and decrement operator
public class MCQ
{
	public static void main(String[] args) {
		int i=7;
		System.out.println(++i); // i=8 output: 8
		System.out.println(i++); // i=8 output: 8 then i=9
		System.out.println(i--); // i=9 output: 9 then i=8
		System.out.println(--i); // i=7 output: 7 
	}
}

/*
OUTPUT:
8
8
9
7
*/